% -------------------------------------------------------------------------
% EXAMPLE 3 -- Halley fractal of x^3 - 1.
% -------------------------------------------------------------------------

f = @(x) [x.^3 - 1; % f(x)   = x^3 - 1
          3*x.^2;   % f'(x)  = 3x^2
          6*x];     % f''(x) = 6x

workingDir = '~/Example3/';
      
halleyFractal(f, workingDir);